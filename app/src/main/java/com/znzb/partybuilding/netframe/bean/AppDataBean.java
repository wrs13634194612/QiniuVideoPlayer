package com.znzb.partybuilding.netframe.bean;

public class AppDataBean {
    /**
     * msg : 请求成功
     * code : 1
     * point : 0
     */

    private String msg;
    private int code;
    private int point;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public int getPoint() {
        return point;
    }

    public void setPoint(int point) {
        this.point = point;
    }
}
